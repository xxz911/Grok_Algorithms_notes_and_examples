# Когда функция вызывает сама себя - это рекурсия
# Рекурсивная функция состоит из двух частей
# 1. Рекурсивный случай - функция вызывает сама себя
# 2. Базовый случай - функция НЕ вызывает сама себя,
# чтобы предотвратить зацикливание

def countdown(i):
    print(i)
    # Базовый случай
    if i <= 1:
        return
    # Рекурсивный случай
    else:
        countdown(i-1)

countdown(8)


